    public void populateTable() {
        DefaultTableModel model1 = (DefaultTableModel) tblStudentRecords1.getModel();
        DefaultTableModel model2 = (DefaultTableModel) tblStudentRecords2.getModel();
        model1.setRowCount(0);
        model2.setRowCount(0);
        if (request.getStudent() != null) {
            Object[] row = new Object[8];
            row[0] = request.getStudent().getName();
            row[1] = request.getStudent().getDob();
            row[2] = request.getStudent().getCity();
            row[3] = request.getStudent().getCurrentAddress();
            row[4] = request.getStudent().getSSNNo();
            row[5] = request.getStudent().getContactNo();
            row[6] = request.getStudent().getEmailAddress();
            row[7] = request.getStudent().getDependents();
            model1.addRow(row);
            Object[] rowSecond = new Object[9];
            rowSecond[0] = request.getStudent().getQualifications();
            rowSecond[1] = request.getStudent().getAccomplishments();
            rowSecond[2] = request.getStudent().getIncome();
            rowSecond[3] = request.getStudent().getEmploymentStatus();
            rowSecond[4] = request.getStudent().getGender();
            rowSecond[5] = request.getStudent().isHasCriminalRecord() ? "Yes" : "No";
            rowSecond[6] = request.getStudent().isMajorIllness() ? "Yes" : "No";
            rowSecond[7] = request.getStudent().isCarrierDisease() ? "Yes" : "No";
            rowSecond[8] = request.getStudent().isGeneticDisease() ? "Yes" : "No";
            model2.addRow(rowSecond);

        }

    }

