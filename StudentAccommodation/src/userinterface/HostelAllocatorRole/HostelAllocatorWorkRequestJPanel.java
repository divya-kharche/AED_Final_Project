    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(request.getStatus().equals("Completed")){
            JOptionPane.showMessageDialog(null,"This request has already been completed by you!");
            return;
        }
        if(txtFieldAddress.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Enter the address field to proceed the request!");
            return;
        }
        request.setTestResult("Sent to SA");
        request.setStatus("Completed");
        UserAccount account = new UserAccount();
        account.setUsername("None");
        request.setReceiver(account);
        request.setTestResult("Completed by Hostel Allocator and passed to Hostel Facilitator");
        ShelterFacilitateWorkRequest sfRequest = new ShelterFacilitateWorkRequest();
        sfRequest.setMessage("Shelter Facilitation");
        sfRequest.setShelterAllocWorkReq(request);
        sfRequest.setStudent(request.getStudent());
        sfRequest.setAddressToBePlaced(txtFieldAddress.getText());
        sfRequest.setSender(userAccount);
        sfRequest.setStatus("Sent");
        Date d = new Date();
        sfRequest.setRequestDate(d);
        Organization org = null;

        for (Network network : business.getNetworkList()) {
            //Step 2.a: check against each enterprise
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise instanceof HostelEnterprise) {
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (organization instanceof HostelFacilitationOrg) {
                            org = organization;
                            break;
                        }
                    }
                }
            }
        }
        if (org != null) {
            org.getWorkQueue().getWorkRequestList().add(sfRequest);
            userAccount.getWorkQueue().getWorkRequestList().add(sfRequest);
            JOptionPane.showMessageDialog(this, "Request Sent to Hostel Facilitator");

        }
    }//GEN-LAST:event_btnSubmitActionPerformed

